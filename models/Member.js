const mongoose = require('mongoose')


const memberSchema = mongoose.Schema({
    user_name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
})


const Member = mongoose.model('members', memberSchema)

module.exports = Member